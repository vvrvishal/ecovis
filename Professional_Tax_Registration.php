            <h1 class="mb-3 mt-5 primaryHeading text-danger">What is Professional Tax?</h1>
            <p class="normalText">Professional tax is a state-level tax that applies to salaried employees and professionals, including chartered accountants, lawyers, and doctors.</p>
            <p class="normalText">Professional tax has to be paid compulsorily by every member of staff who is employed in any private firm operating in India. All business owners must register for professional tax and take up responsibility for deducting professional tax and paying for it.</p>

            <h1 class="mb-3 mt-5 primaryHeading text-danger">PROFESSIONAL TAX VARIATION WITH EACH STATE</h1>
            <p class="normalText">The rate of professional tax varies from state to state. The state governments which are levying state tax are West Bengal, Maharashtra, Karnataka, Kerala, Assam, Madhya Pradesh, Orissa, Tripura, Meghalaya, and Gujarat.</p>

            <h1 class="mb-3 mt-5 primaryHeading text-danger">PROFESSIONAL TAX FOR SELF-EMPLOYED</h1>
            <p class="normalText">Any professional who earns a regular monthly income must pay this tax. Even those offering professional freelance services must pay professional tax using a special form. They must first get a registration number and then pay their taxes in the corresponding banks using the number.</p>

            <h1 class="mb-3 mt-5 primaryHeading text-danger">EXEMPTION FROM PROFESSIONAL TAX PAYMENT</h1>
            <p class="normalText">The following individuals are exempted from professional tax:</p>

